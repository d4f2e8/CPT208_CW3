    private void bookNowBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookNowBtnActionPerformed
        String hotelName = selectedHotelLabel.getText();
        String hotelAddress = selectedAddressLabel.getText();
        String roomType = null;
        Enumeration<AbstractButton> roomTypeBtns = RoomTypeBtnGroup.getElements();
        while (roomTypeBtns.hasMoreElements()) {
            AbstractButton modelbtn = roomTypeBtns.nextElement();
            if (modelbtn.isSelected()) {
                roomType = modelbtn.getText();
                break;
            }
        }
        if (roomType == null) {
            JOptionPane.showMessageDialog(this, "Please select one room type.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int roomNum = Integer.parseInt((String) roomNumComboBox.getSelectedItem());
        int peopleNum = Integer.parseInt((String) peopleNumComboBox.getSelectedItem());
        //start date
        if (startYearBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the start year.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String startYear = (String) startYearBox.getSelectedItem();
        if (startMonthBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the start month.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String startMonth = (String) startMonthBox.getSelectedItem();
        if (startDayBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the start day.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String startDay = (String) startDayBox.getSelectedItem();
        //end date
        if (endYearBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the end year.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String endYear = (String) endYearBox.getSelectedItem();
        if (endMonthBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the end month.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String endMonth = (String) endMonthBox.getSelectedItem();
        if (endDayBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Please select the end day.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String endDay = (String) endDayBox.getSelectedItem();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date startDate = new Date(Integer.parseInt(startYear) - 1900, Integer.parseInt(startMonth) - 1, Integer.parseInt(startDay));
        String formatStartDate = getDate(startDate);
        Date endDate = new Date(Integer.parseInt(endYear) - 1900, Integer.parseInt(endMonth) - 1, Integer.parseInt(endDay));
        String formatEndDate = getDate(endDate);
        if (startDate.before(today)) {
            JOptionPane.showMessageDialog(this, "The start date shouldn't before today.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (endDate.before(startDate)) {
            JOptionPane.showMessageDialog(this, "The end date should after start date.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String bookerName = bookerNameField.getText();
        if (bookerName.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the booker name.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String phoneNum = phoneNumField.getText();
        phoneNum = phoneNum.replaceAll("[^\\d]", "");
        if (phoneNum.equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter the phone number.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (phoneNum.length() != 11) {
            JOptionPane.showMessageDialog(this, "Please enter the correct phone number.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int quitVal = JOptionPane.showConfirmDialog(this, "Do you want to submit this booking?", "Notice", JOptionPane.YES_NO_OPTION);
        if (quitVal == 0) {
            Booking booking = new Booking(hotelName, hotelAddress, roomType, roomNum, peopleNum, formatStartDate, formatEndDate, bookerName, phoneNum);
            bookings.add(booking);
            if (hotelName.equals("Green Hotel")) {
                if (roomType.equals("Single Room")) {
                    hotels[0].setRoom1Num(hotels[0].getRoom1Num() - roomNum);
                }
                if (roomType.equals("Double Room")) {
                    hotels[0].setRoom2Num(hotels[0].getRoom2Num() - roomNum);
                }
                if (roomType.equals("Standard Room")) {
                    hotels[0].setRoom3Num(hotels[0].getRoom3Num() - roomNum);
                }
            } else if (hotelName.equals("White Hotel")) {
                if (roomType.equals("Single Room")) {
                    hotels[1].setRoom1Num(hotels[1].getRoom1Num() - roomNum);
                }
                if (roomType.equals("Double Room")) {
                    hotels[1].setRoom2Num(hotels[1].getRoom2Num() - roomNum);
                }
                if (roomType.equals("Standard Room")) {
                    hotels[1].setRoom3Num(hotels[1].getRoom3Num() - roomNum);
                }
            }
            System.out.println(bookings.get(0).toString());
            System.out.println("Booking successfully");
            initBookingForm();
            JOptionPane.showMessageDialog(this, "Booking successfully. You can check your booking in \"My Reservation\".       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            bookingNow = 0;
            bookPanel.setVisible(true);
            hotelDetailPanel.setVisible(true);
        }
        if (quitVal == 1) {
            return;
        }


    }//GEN-LAST:event_bookNowBtnActionPerformed

    private void mapLabelMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mapLabelMouseMoved
        Point point = evt.getLocationOnScreen();
        Point jFrameLocation = this.getLocation();
        if (point.x >= 306 + jFrameLocation.x && point.x <= 343 + jFrameLocation.x && point.y >= 255 + jFrameLocation.y && point.y <= 317 + jFrameLocation.y) {
            setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
            showImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/WhiteHotel.png")));
            showNameLabel.setText("White Hotel");
        } else if (point.x >= 506 + jFrameLocation.x && point.x <= 545 + jFrameLocation.x && point.y >= 333 + jFrameLocation.y && point.y <= 393 + jFrameLocation.y) {
            setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
            showImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/img/GreenHotel.png")));
            showNameLabel.setText("Green Hotel");
        } else {
            showImageLabel.setIcon(null);
            showNameLabel.setText(null);
            setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
        }
    }//GEN-LAST:event_mapLabelMouseMoved

