    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        DefaultListModel defaultListModel = (DefaultListModel) bookingInfoList.getModel();
        if(bookingInfoList.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Please select a reservation before deleting.       ", "Notice", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Booking booking = bookings.get(bookingInfoList.getSelectedIndex());
        if (booking.getHotelName().equals("Green Hotel")) {
            if (booking.getRoomType().equals("Single Room")) {
                hotels[0].setRoom1Num(hotels[0].getRoom1Num() + booking.getRoomNum());
            }
            if (booking.getRoomType().equals("Double Room")) {
                hotels[0].setRoom2Num(hotels[0].getRoom2Num() + booking.getRoomNum());
            }
            if (booking.getRoomType().equals("Standard Room")) {
                hotels[0].setRoom3Num(hotels[0].getRoom3Num() + booking.getRoomNum());
            }
        } else if (booking.getHotelName().equals("White Hotel")) {
            if (booking.getRoomType().equals("Single Room")) {
                hotels[1].setRoom1Num(hotels[1].getRoom1Num() + booking.getRoomNum());
            }
            if (booking.getRoomType().equals("Double Room")) {
                hotels[1].setRoom2Num(hotels[1].getRoom2Num() + booking.getRoomNum());
            }
            if (booking.getRoomType().equals("Standard Room")) {
                hotels[1].setRoom3Num(hotels[1].getRoom3Num() + booking.getRoomNum());
            }
        }
        bookings.remove(bookingInfoList.getSelectedIndex());
        defaultListModel.remove(bookingInfoList.getSelectedIndex());
        if (bookings.size() == 0) {
            totalBookingNumLabel.setText("You have no booking now");
        } else {
            totalBookingNumLabel.setText("You have " + bookings.size() + " booking totally");
        }
        JOptionPane.showMessageDialog(this, "Delete successfully.", "Notice", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_removeBtnActionPerformed

    private void DiscardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DiscardActionPerformed
        int quitVal = JOptionPane.showConfirmDialog(this, "Do you want to discard the recent booking?", "Notice", JOptionPane.YES_NO_OPTION);
        if (quitVal == 0) {
            initBookingForm();
            hotelDetailPanel.setVisible(true);
            bookPanel.setVisible(true);
            bookingNow = 0;
        }
    }//GEN-LAST:event_DiscardActionPerformed

